package javaDataTypes;

public class LoopingConsepts {

	public static void main(String[] args) {

		// print 1 to 10

		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}

		System.out.println("******");
		// print 10 to 1

		for (int i = 10; i >= 1; i--) {
			System.out.println(i);
		}

		// print 10 to -10

		for (int j = 10; j >= -10; j--) {
			System.out.println(j);
		}

	}

}
